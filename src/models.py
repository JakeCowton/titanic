class Passenger(object):

    p_id = None
    survival = None
    p_class = None
    name = None
    sex = None
    age = None
    n_sib_sp = None
    n_pa_ch = None
    ticket = None
    fare = None
    cabin = None
    embarked = None

    def __init__(p_id ,survival, p_class, name, sex, age, n_sib_sp, n_pa_ch,
                 ticket, fare, cabin, embarked):
        p_id = p_id
        survival = survival
        p_class = p_class
        name = name
        sex = sex
        age = age
        n_sib_sp = n_sib_sp
        n_pa_ch = n_pa_ch
        ticket = ticket
        fare = fare
        cabin = cabin
        embarked = embarked
